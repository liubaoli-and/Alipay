package ustb.edu.cn.alipay;

public class Alipay {
	private boolean ia;
	private int actualName;
	private double freeWithdrawalLimit;
	private double moneyInBalance;
	private double moneyInYuebao;
	private int bandCard;
	private int name;
	private  double transferAmount;
	private int transferMethod;
	private int cardID;
	private double moneyInBankCard;
	
	public boolean isIa() {
		return ia;
	}
	public void setIa(boolean ia) {
		this.ia = ia;
	}
	public int getActualName() {
		return actualName;
	}
	public void setActualName(int actualName) {
		this.actualName = actualName;
	}
	public double getFreeWithdrawalLimit() {
		return freeWithdrawalLimit;
	}
	public void setFreeWithdrawalLimit(double freeWithdrawalLimit) {
		this.freeWithdrawalLimit = freeWithdrawalLimit;
	}
	public double getMoneyInBalance() {
		return moneyInBalance;
	}
	public void setMoneyInBalance(double moneyInBalance) {
		this.moneyInBalance = moneyInBalance;
	}
	public double getMoneyInYuebao() {
		return moneyInYuebao;
	}
	public void setMoneyInYuebao(double moneyInYuebao) {
		this.moneyInYuebao = moneyInYuebao;
	}
	public int getBandCard() {
		return bandCard;
	}
	public void setBandCard(int bandCard) {
		this.bandCard = bandCard;
	}
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public double getTransferAmount() {
		return transferAmount;
	}
	public void setTransferAmount(double transferAmount) {
		this.transferAmount = transferAmount;
	}
	public int getTransferMethod() {
		return transferMethod;
	}
	public void setTransferMethod(int transferMethod) {
		this.transferMethod = transferMethod;
	}
	public int getCardID() {
		return cardID;
	}
	public void setCardID(int cardID) {
		this.cardID = cardID;
	}
	public double getMoneyInBankCard() {
		return moneyInBankCard;
	}
	public void setMoneyInBankCard(double moneyInBankCard) {
		this.moneyInBankCard = moneyInBankCard;
	}
	
}
